<h1>Đặt lại mật khẩu</h1>

<p>Bạn có thể đặt lại mật khẩu bằng cách nhấn vào liên kết bên dưới:</p>
<a href="http://localhost:5173/user/reset-password?token={{ $token }}&email={{ $email }}">
  Nhấn vào đây để đặt lại mật khẩu
</a>

<p>Nếu bạn không yêu cầu đặt lại mật khẩu, hãy bỏ qua email này.</p>
